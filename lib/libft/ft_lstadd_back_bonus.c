#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*aux;

	aux = *lst;
	if (!lst || !new)
		return ;
	if (aux == NULL)
	{
		*lst = new;
		return ;
	}
	while (aux -> next)
		aux = aux -> next;
	aux->next = new;
}
